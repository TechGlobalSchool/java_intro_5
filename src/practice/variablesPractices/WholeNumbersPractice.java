package practice.variablesPractices;

public class WholeNumbersPractice {

    public static void main(String[] args){
        byte number1 = 9;
        short number2 = 23;
        int number3 = 2;
        long number4 = 12345678999L;

        System.out.println("number 1 = \"" + number1 + "\"");
        System.out.println("number 2 = \"" + number2 + "\"");
        System.out.print("Number 3 = \"" + number3 + "\"\n");
        System.out.println("Number 4 = \"" + number4 + "\"");

    }

}
