package loops.fori_loops;

public class Exercise01_PrintNumbersAscending {
    public static void main(String[] args) {
        /*
        Write a Java program to print numbers from 1 to 10 (1 and 10 are included)

        Expected output:
        1
        2
        .
        .
        .
        8
        9
        10

        start point: 1
        end point: 10
        update: increment

         */

        for(int j = 1; j < 11; j++){
            System.out.println(j);
        }

    }
}
